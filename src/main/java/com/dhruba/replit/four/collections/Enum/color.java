package four.collections.Enum;

/*An enum is a special class representing group of constants separated by comma and in 
 * upper case
 1. The constants of an enum are final
 2. enum cannot extend other classes but can implement interfaces
 3. enum objects cannot be created so no need of constructors
 4. can contain only concrete methods
 5. Used for values which will not change*/
public enum color {
	RED, GREEN, BLUE, YELLOW, BLACK

}
